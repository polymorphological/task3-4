x = int(input('x = '))

# Замінимо другий вкладений if у попередньому прикладі на оператор розгалуження з кількома умовами
if 0 < x < 7:
    print('Значення x входить у заданий діапазон, продовжуємо')
    y = 2 * x - 5
    if y < 0:
        print('Значення y негативне')
    elif y > 0:
        print('Значення y позитивне')
    else:
        print('y = 0')

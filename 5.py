# Змінні – імена, які прив'язуються до об'єктів
var = 'I am a string'
print(var)
# type(var) повертає тип об'єкта, на який посилається var
print(type(var))

print()

# Їх можна прив'язувати до нових значень
var = 42
print(var)
print(type(var))

print()


# Приклади дійсних чисел
a = 0.5
b = 3.2

# Приклади дійсних чисел в експоненційному записі
c = 3.2e5  # то же самое, что и 3.2 * 10 ** 5
d = 1e-3   # то же самое, что и 1 * 10 ** (-3)

print(a, b, c, d)

# Побудова дійсного значення
# з рядку
